import random

PATTERN_RU = {
    "control": "контроль",
    "avoid": "уход",
    "react": "реакция",
    "analyze": "анализ",
}

CBT = {
    "control": "если не проконтролирую — плохо",
    "avoid": "лучше не лезть",
    "react": "надо сразу",
    "analyze": "надо всё обдумать",
}

DISTORTION = {
    "control": "гиперконтроль",
    "avoid": "избегание",
    "react": "импульс",
    "analyze": "зацикливание",
}


MANIFESTATIONS = {
    "control": [
        "держишь внутри",
        "контролируешь ситуацию",
        "перегружаешься",
        "не проговариваешь",
    ],
    "avoid": [
        "откладываешь решения",
        "уходишь",
        "избегаешь разговора",
        "возвращаешься к мыслям",
    ],
    "react": [
        "реагируешь резко",
        "включаешься сразу",
        "делаешь импульсивно",
        "перегораешь",
    ],
    "analyze": [
        "возвращаешься к мыслям",
        "перегружаешься мыслями",
        "сомневаешься",
        "откладываешь",
    ],
}


def build_block1(main):
    variants = [
        f"""Когда возникает напряжение,
ты не решаешь ситуацию сразу.

Ты используешь привычную стратегию — {PATTERN_RU[main]}.

В этот момент ты не работаешь с ситуацией,
ты пытаешься справиться с внутренним состоянием.

И поэтому причина остаётся,
а напряжение возвращается снова.""",

        f"""В напряжённых ситуациях ты не идёшь в прямое действие.

Сначала включается автоматическая реакция — {PATTERN_RU[main]}.

Она помогает временно снизить напряжение,
но не решает саму ситуацию.

В итоге ты оказываешься в повторяющемся цикле."""
    ]
    return random.choice(variants)


def build_block2(archetype, second):
    variants = [
        f"""Снаружи ты проявляешься как {archetype}.

Но внутри включается другая часть — {PATTERN_RU[second]}.

Одна часть тебя хочет двигаться вперёд,
другая — тормозит или уводит в сторону.

Из-за этого возникает внутреннее напряжение.""",

        f"""Ты воспринимаешь себя как {archetype} —
человека, который может действовать.

Но внутри работает другой механизм — {PATTERN_RU[second]}.

Он замедляет тебя, заставляет сомневаться
или уходить от действия.

Так формируется внутренний конфликт."""
    ]
    return random.choice(variants)


def build_block3(main, second):
    base = MANIFESTATIONS[main][:3]
    extra = MANIFESTATIONS[second][0]
    items = base + [extra]
    return "\n".join([f"— {i}" for i in items])


def build_block4(main):
    variants = [
        f"""В основе твоих реакций лежит автоматическая мысль:

{CBT[main]}

Ты её не замечаешь,
но именно она запускает твоё поведение.

Из-за этого возникает искажение:

{DISTORTION[main]}

Ты реагируешь не на ситуацию,
а на её интерпретацию.""",

        f"""Твоя реакция начинается не с события,
а с мысли, которая возникает автоматически:

{CBT[main]}

Она формирует искажение:

{DISTORTION[main]}

И именно оно управляет твоими действиями.

Не ситуация — а её восприятие."""
    ]
    return random.choice(variants)


def build_block5():
    variants = [
        """Твоя ключевая точка — момент до реакции.

Когда возникает напряжение,
у тебя есть короткий промежуток:

1–3 секунды

В этот момент ты можешь заметить
свою автоматическую реакцию.""",

        """Самое важное происходит не в ситуации,
а прямо перед реакцией.

У тебя есть несколько секунд,
прежде чем включится привычный сценарий.

И именно там находится точка изменения."""
    ]
    return random.choice(variants)


def build_block6(main):
    variants = [
        f"""Сегодня ты, скорее всего, заметишь:

→ {PATTERN_RU[main]}

Когда это произойдёт,
задать себе вопрос:

«я сейчас реагирую или выбираю?»""",

        f"""Обрати внимание сегодня:

в момент напряжения у тебя появится
желание → {PATTERN_RU[main]}

Не меняй это.

Просто зафиксируй:

это автоматическая реакция."""
    ]
    return random.choice(variants)


def build_block7():
    variants = [
        """Ты не «такой человек».

Ты действуешь по сценарию,
который можно увидеть и изменить.""",

        """Это не черта характера.

Это повторяющийся механизм.

И теперь ты его видишь."""
    ]
    return random.choice(variants)


def build_deep_result(main, second, archetype):

    block1 = build_block1(main)
    block2 = build_block2(archetype, second)
    block3 = build_block3(main, second)
    block4 = build_block4(main)
    block5 = build_block5()
    block6 = build_block6(main)
    block7 = build_block7()

    part1 = f"{block1}\n\n{block2}"
    part2 = f"{block3}\n\n{block4}"
    part3 = f"{block5}\n\n{block6}\n\n{block7}"

    return {
        "part1": part1,
        "part2": part2,
        "part3": part3,
    }
