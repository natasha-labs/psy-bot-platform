from collections import Counter
from tests.shadow.questions import questions


SHADOW_LABELS = {
    "control": "Контроль",
    "weakness": "Уязвимость",
    "anger": "Агрессия",
    "fear": "Страх",
}

SHADOW_TYPES = {
    "control": "Контролёр",
    "weakness": "Ранимый",
    "anger": "Бунтарь",
    "fear": "Тревожный",
}


def calculate_profile(answer_values):
    counts = Counter(answer_values)

    for key in SHADOW_LABELS:
        if key not in counts:
            counts[key] = 0

    total = sum(counts.values()) or 1

    percentages = {
        key: round(counts[key] / total * 100)
        for key in SHADOW_LABELS
    }

    sorted_profiles = sorted(
        percentages.items(),
        key=lambda item: item[1],
        reverse=True,
    )

    main_type = sorted_profiles[0][0]
    second_type = sorted_profiles[1][0]

    return percentages, main_type, second_type


